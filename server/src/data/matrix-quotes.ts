// ─── Helpers ───────────────────────────────────────────────────

/** Get a specific quote by its named key. */
export function getQuote(key: QuoteKey): string {
  return Q[key];
}

/** Get a random quote, optionally from a specific category. */
export function randomQuote(category?: keyof typeof QUOTES): string {
  const pool = category ? QUOTES[category] : QUOTES.general;
  return pool[Math.floor(Math.random() * pool.length)];
}
